fun main() {
    //  While loop
    // A program able to print from 0 to 10

    var count = 0

    while(count <= 10){
        println("The number is: $count")
        count = count + 1
    }

    println("===================")

    // While loop printing from 50 to 35 with the steps of 2
    var c = 50
    while (c >= 35){
        println("The number is: $c")
        c = c - 2
    }
}