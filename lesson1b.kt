fun main() {
    // Type inference - where the compiler is left to decide the data type of data entered.
    // Below variables are declared and their data types stated.
    val number1 : Int = 5
    val number2 : Double = 3.142
    val letter : Char = 'P'
    val paidLoan : Boolean = false
    val name : String = "Max"

    // A Char represents a single 16-bit Unicode character. It’s the smallest unit of text.
    // Syntax: Always enclosed in single quotes ('A').
    // Capacity: Exactly one character. You cannot have an empty Char (e.g., '' is an error), and you cannot have multiple characters inside it.

    // Print out the output
    println(number1)
    println(number2)
    println(letter)
    println(paidLoan)
    println(name)
}