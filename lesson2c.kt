fun main() {
    //  IF ...else if...else statement
    // Used to evaluate multiple conditions. The else part gets executed when all the conditions given are false.

    val number = 0

    if (number > 0) {
        println("The number is positive")
    }
    else if (number == 0) {
        println("The number is zero")
    }
    else {
        println("The number is negative")
    }
}